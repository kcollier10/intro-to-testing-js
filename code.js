function helloWorld() {
    return "Hello, World!";
}

function hiEveryone() {
    return "Hi, everyone!";
}

function sayHello(input) {
    if (input === true || input === false || input === "" || input === undefined) {
        return "Hello, World!";
    }
    return "Hello, " + input + "!";
}



